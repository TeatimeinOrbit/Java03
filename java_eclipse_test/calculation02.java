package java_eclipse_test;

public class calculation02 {
	public static void main(String[] args) {
		int a = 10;
		a++;
		System.out.println(a);
		int b = 10;
		b --;
		System.out.println(b);
		
		System.out.println("Next");
		
		int c = 10;
		c += 2;
		System.out.println(c);
		c -= 2;
		System.out.println(c);
		c *= 2;
		System.out.println(c);
		c /= 2;
		System.out.println(c);
		c %= 2;
		System.out.println(c);
	}

}
